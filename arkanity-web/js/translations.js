// Expose to window for main.js access
window.translations = translations;

let currentLang = localStorage.getItem('arkanity-lang') || 'bg';

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('arkanity-lang', lang);

    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            element.innerHTML = translations[lang][key];
        }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (translations[lang] && translations[lang][key]) {
            element.placeholder = translations[lang][key];
        }
    });

    const langToggle = document.getElementById('lang-toggle');
    if (langToggle) {
        langToggle.innerHTML = lang === 'bg' ? '<span>üáßüá¨</span> BG' : '<span>üá¨üáß</span> EN';
    }

    document.documentElement.lang = lang;
}

document.addEventListener('DOMContentLoaded', () => {
    setLanguage(currentLang);

    const langToggle = document.getElementById('lang-toggle');
    const langToggleMobile = document.getElementById('lang-toggle-mobile');

    if (langToggle) {
        langToggle.addEventListener('click', () => {
            setLanguage(currentLang === 'bg' ? 'en' : 'bg');
        });
    }

    if (langToggleMobile) {
        langToggleMobile.addEventListener('click', () => {
            setLanguage(currentLang === 'bg' ? 'en' : 'bg');
        });
    }
});
